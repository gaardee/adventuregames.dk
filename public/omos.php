  <section id="tekst_om">
    <h1>Hvem er vi?</h1>
    <p>
      Adventuregames er underholdning med plads til alle. Sammen med vores kunder skaber vi uforglemmelige dage og vedvarende minder. Vi tror ikke på begrænsninger, men på at skræddersy arrangementer til enhver lejlighed. Adventuregames er for alle
      os der elsker at have det sjovt.
      <br />
      <br />
      Vi arrangerer events- og teambuildingaktiviter for private og virksomheder. For os handler det om, at alle føler sig godt tilpas og får god en oplevelse. Vores aktiviteter kan varieres så de udfordrer tænkeren, den stærke og dem som ønsker en
      mental udfordring.
    </p>

    <h2>Pædagogisk baggrund</h2>
    <p>
      Adventuregames består af Peter Hartvigsen – Uddannet pædagog og grundlægger. Vores baggrund sikrer at aktiviteterne er velegnede til alle aldre. Vi kan differentiere vores aktiviteter så de passer til de konkurrencemindede idrætsudøvere eller
      har mere fokus på samarbejdet på holdet. Udover de fastansatte har vi tilknyttet instruktører, som har baggrund i Idræt eller pædagogik.
    </p>

    <h2>Sikkerhed</h2>
    <p>
      Hos Adventuregames er sikkerheden første prioritet. Alle instruktører er grundigt uddannede i alle aktiviteter og vi benytter os af certificeret udstyr, vi medbringer altid førstehjælpsudstyr. Vores klatreinstruktører har certificeret
      uddannelse.
    </p>

    <h2>Vores Vision</h2>
    <p>
      Adventuregames er underholdning med plads til alle. Sammen med vores kunder skaber vi uforglemmelige dage og vedvarende minder. Vi tror ikke på begrænsninger, men på at skræddersy arrangementer til enhver lejlighed. Adventuregames er for alle
      os der elsker at have det sjovt.<br />
      <br />
    </p>
    <h2>Det gør vi ved:</h2>
    <p>
      - Altid at besvare henvendelser med personlige svar<br />
      - Besvare henvendelser indenfor 24 timer<br />
      - Tilbyde aktiviteter som passer til kundens ønsker<br />
      - Tilbyde aktiviteter hvor instruktørerne har en personlig kontakt med deltagerne
    </p>
