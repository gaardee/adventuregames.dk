<section id="tekst_stratego">
    <h1>Stratego</h1>
    <p>Det kendte brætspil – Blot med jer som brikker!</p>
    <br>
    <p>Stratego bringer legelysten op i alle. Fordelen er, at alle kan være med, uanset alder. Vil man gerne køre i et lavere gear, eller har man lyst til at give den gas.</p>
    <br>
    <p>Kommunikationen er afgørende for at kunne vinde.</p>

    <h1>Varighed</h1>
    <p>2 timer.</p>
    <h1>Sted</h1>
    <p>Stratego kan spilles alle steder, hvor der er lidt plads (som en stor græsplæne). Adventure Games kommer gerne ud til jer.</p>
    <h1>Pris</h1>
    <p>Min. 21 personer.</p>
    <br>
    <p>300,00 pr. person.</p>
    <br>
    <p>Hvis I er mere end 40 personer, kan der opnås rabat.</p>
    <h1>Kort fortalt</h1>
    <p>Spillet går ud på, at I bliver delt op i mindre hold, der dyster mod hinanden.</p>
    <br>
    <p>Hvert hold udvælger en “feltmarskal” som leder holdet.</p>
    <br>
    <p>Hver spiller på holdet får en rolle: Bombe, spion til General – Som i stratego.</p>
    <br>
    <p>Holdets feltmarskal skal fordele kortene til spillerne, som skal vinde kortene fra de andre hold. Det gør man ved snedighed, snilde og billige tricks.</p>
    <br>
    <p>Mellem de to Stratego-spillerunder på ca. 30 minutter er der små aktivitetspauser, hvor holdene prøver kræfter på andre måder – her dystes der på kløgt, styrke, balance og hurtighed. Til sidst findes et vindende hold, der selvfølgelig får en præmie.</p>

</section>