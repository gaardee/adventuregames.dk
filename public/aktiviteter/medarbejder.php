    <h1>Årets medarbejder</h1>
    <p>Her er det dagsformen, heldet og evnen til at finde gode samarbejdspartnere der sætter trofæet hjem i skabet.</p>
    <br>
    <p>Vi lægger blødt ud med at give hver deltager 30 Adventure Dollars. Dem har de nu 1 time til at få til at yngle i vores Casino, hvor der vil være Roulette, samt Blackjack (Terningespil, kan også sættes på, ved køb af ekstra croupiér)</p>
    <br>
    <p>De 5 deltagere der har fået sine dollars til at yngle bedst, går direkte i finalen. 3 deltagere mere bliver fundet via 3 små lynkonkurrencer.</p>
    <br>
    <p>Derefter er det finale a’ la Robinson, den der kommer sidst i de forskellige konkurrencer, ryger ud. der vil være 2 gange hvor der bliver mulighed for de deltagere, der ikke nåede til finalen, at komme ind i spillet igen (lucky loosers).</p>
    <br>
    <p>Finaleaktiviteterne er egnede til festtøj og de vil veksle mellem, kløgt, held, præcision, hukommelse samt mobilitet.</p>

    <h1>Varighed</h1>
    <p>2 timer uanset antal</p>

    <h1>Antal</h1>
    <p>Min. 30 personer.</p>

    <h1>Pris</h1>
    <p>500,00 pr. deltager.</p>
    <br>
    <p>Alle priser er eksklusiv moms</p>

</section>