    <h1>Fangerne på Fortet</h1>
    <p>Fangerne på fortet henvender sig til firmaer og private. Velfungerende som teambuilding for større og mindre
        grupper.

        Har I lyst til en dag med sjove aktiviteter, med plads til alle, er dette aktiviteten I skal vælge.</p>

    <h1>Varighed</h1>
    <p>Mellem 2-3½ time alt efter deltagerantal.</p>
    <h1>Sted</h1>
    <p>Vi har flere faste lokaliteter i både København og Århus.

        Vores setup er mobilt – Vi kommer gerne til jer. Dog skal der betales transport udenfor København.</p>
    <h1>Pris</h1>
    <p>Min. 15 personer.
        Alm. udgave [12 baner] 550 kr. pr. deltager
        Stor udgave [18 baner] 700 kr. pr. deltager

        Rabat gives ved flere end 40 deltagere, ring og hør nærmere.

        Alle priser er ekskl. moms</p>
    <h1>Kort fortalt</h1>
    <p>Konceptet er stærkt inspireret af TV3’s populære underholdningsprogram.

        I bliver delt op i hold, der dyster imod hinanden.

        Hvert hold får tildelt en instruktør – en “passepartout”.

        I skal igennem forskellige typer af baner. Bl.a. slanger, masser af vand, balanceopgaver, gå på glasskår mm.

        Hver bane giver enten en nøgle eller en ledetråd, som skal bruges i skatkammeret.

        Afslutningsvis skal I til skatkammeret og samle guldmønter – det hold der samler mest guld har vundet.</p>
    <h1>Ufordringer for alle</h1>
    <p>I skal igennem enten 12 eller 18 baner.

        I vælger selv banerne ud (oversigt kan tilsendes pr. mail)

        Der er brug for alle – både tænkeren og muskelbundtet – ligesom I skal være gode til at samarbejde.

        Alle hold får, uanset om I er 15 eller 700 deltagere, en personlig service.

        Det er vigtigt for os, at alle føler, at de er en del af arrangementet.</p>

    <h1>Vi tilbyder</h1>

    <p>- Gamemaster
        <br>- Instruktør til hvert hold.
        <br>- Uddannede specialinstruktører til banerne med slanger eller klatring.
        <br>- Mulighed for selv at vælge baner.
        <br>- Præmier til vinderne.
    </p>
