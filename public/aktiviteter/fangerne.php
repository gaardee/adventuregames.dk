<section id="tekst_fangerne">
    <h1>Fangerne på Fortet</h1>
    <p>Fangerne på fortet henvender sig til firmaer og private. Velfungerende som teambuilding for større og mindre
        grupper.</p>
    <br>
    <p>Har I lyst til en dag med sjove aktiviteter, med plads til alle, er dette aktiviteten I skal vælge.</p>
    <br>
    <p>Uddybende materiale ved kontakt</p>

    <h1>Varighed</h1>
    <p>Mellem 2-3½ time alt efter deltagerantal.</p>

    <h1>Sted</h1>
    <p>Vi har flere faste lokaliteter i både København og Århus.</p>
    <br>
    <p>Vores setup er mobilt – Vi kommer gerne til jer. Dog skal der betales transport udenfor København og Århus.</p>

    <h1>Pris</h1>
    <p>Min. 15 personer.</p>
    <br>
    <p>Alm. udgave [12 baner] 550,00 pr. deltager</p>
    <p>Stor udgave [18 baner] 700,00 pr. deltager</p>
    <br>
    <p>Rabat gives ved flere end 40 deltagere, ring og hør nærmere.</p>
    <br>
    <p>Alle priser er ekskl. moms</p>

    <h1>Kort fortalt</h1>
    <p>Konceptet er stærkt inspireret af TV3’s populære underholdningsprogram.</p>
    <br>
    <p>I bliver delt op i hold, der dyster imod hinanden.</p>
    <br>
    <p>Hvert hold får tildelt en instruktør – en “passepartout”.</p>
    <br>
    <p>I skal igennem forskellige typer af baner. Bl.a. slanger, masser af vand, balanceopgaver, gå på glasskår mm.</p>
    <br>
    <p>Hver bane giver enten en nøgle eller en ledetråd, som skal bruges i skatkammeret.</p>
    <br>
    <p>Afslutningsvis skal I til skatkammeret og samle guldmønter – det hold der samler mest guld har vundet.</p>

    <h1>Ufordringer for alle</h1>
    <p>I skal igennem enten 12 eller 18 baner.</p>
    <br>
    <p>I vælger selv banerne ud (oversigt kan tilsendes pr. mail)</p>
    <br>
    <p>Der er brug for alle – både tænkeren og muskelbundtet – ligesom I skal være gode til at samarbejde.</p>
    <br>
    <p>Det er vigtigt for os, at alle føler, at de er en del af arrangementet.</p>

    <h1>Vi tilbyder</h1>

    <p>
        <li>Gamemaster</li>
        <li>Instruktør til hvert hold.</li>
        <li>Uddannede specialinstruktører til banerne med slanger eller klatring.</li>
        <li>Mulighed for selv at vælge baner.</li>
        <li>Præmier til vinderne.</li>
    </p>
</section>