    <h1>Tvekamp</h1>
    <p>Tvekamp går ud på, at I dyster direkte mod hinanden i hold. Der er forskellige discipliner, som er udvalgt på forhånd. Disciplinerne kan bl.a. være bueskydning, øksekast, formel 1 med fjernstyrede biler, sumowrestling eller quiz.

        Et hold er typisk fra tre til ti deltagere og aktiviteterne reguleres
        efter antal. Er der to hold tilmeldt, dystes mod hinanden i syv forskellige discipliner.

        Er der flere end to hold, sørger vi for, at alle hold møder hinanden
        og der efterfølgende er finaler.

        Hver disciplinsejr giver 2 point og der er præmier til det hold med
        flest point.</p>

    <h1>Varighed</h1>
    <p>1 1/2 time uanset antal.</p>
    <h1>Sted</h1>
    <p>Tvekamp kan spilles alle steder, hvor der er lidt plads (som en stor græsplæne). Adventure Games kommer gerne ud til jer.</p>
    <h1>Pris</h1>
    <p>Min. 10 personer.
        500 kr. pr. deltager. Alle priser er eksklusiv moms</p>

