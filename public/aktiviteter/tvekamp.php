<section id="tekst_tvekamp">
    <h1>Tvekamp</h1>
    <p>Tvekamp går ud på, at I dyster direkte mod hinanden i hold. Der er forskellige discipliner, som er udvalgt på forhånd. Disciplinerne kan bl.a. være bueskydning, øksekast, formel 1 med fjernstyrede biler, sumowrestling eller quiz.</p>
    <br>
    <p>Et hold er typisk fra tre til ti deltagere og aktiviteterne reguleres efter antal. Er der to hold tilmeldt, dystes mod hinanden i syv forskellige discipliner.</p>
    <br>
    <p>Er der flere end to hold, sørger vi for, at alle hold møder hinanden og der efterfølgende er finaler.</p>
    <br>
    <p>Hver disciplinsejr giver 2 point og der er præmier til det hold medflest point.</p>
    <br>
    <p>Fungerer rigtigt godt til større selskaber 50-300</p>
    <br>
    <p>Vi laver kæde, som fungerer lidt som et ur, når man har mødt et hold, går det ene hold med uret, det andet mod uret, her møder man et nyt hold i en ny diciplin, hver gang, så de fleste hold når at møde hinanden.</p>

    <h1>Varighed</h1>
    <p>1 1/2 time uanset antal.</p>

    <h1>Sted</h1>
    <p>Tvekamp kan spilles alle steder, hvor der er lidt plads (som en stor græsplæne). Adventure Games kommer gerne ud til jer.</p>

    <h1>Pris</h1>
    <p>Min. 10 personer.</p>
    <br>
    <p>500,00 pr. deltager.</p>
    <br>
    <p>Alle priser er eksklusiv moms</p>

</section>