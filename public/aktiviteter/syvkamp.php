    <h1>Syv-kamp</h1>
    <p>De forskellige hold skal starte med at finde et navn til holdet samt et kampråb. Når de har gjort det sendes de af sted på de syv forskellige aktiviteter.</p>
    <br>
    <p>Aktiviteterne bliver passet ind til holdets størrelse, men dog altid så aktiviteten kan lykkes, også selv om der er en deltager, der ikke har lyst/mod på udfordringen.</p>
    <br>
    <p>Det gælder om at bruge så lidt tid pr. aktivitet så muligt, dog må holdet maksimalt bruge 10 minutter pr. bane. Arrangøren vælger selv de aktiviteter ud, som han/hun mener er passende til deltagerne.</p>

    <h1>Varighed</h1>
    <p>1½ time uanset antal</p>

    <h1>Pris</h1>
    <p>Min. 10 personer.</p>
    <br>
    <p>400,00 pr. deltager.</p>
    <br>
    <p>Alle priser er eksklusiv moms</p>

