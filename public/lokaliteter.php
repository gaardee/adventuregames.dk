    <h1>Lokaliteter</h1>
    <p>Alle vores koncepter er mobile.

        Vi kan altid afholde arrangementet hos jer, såfremt i har passende lokaliteter.

        Krav til lokaliteterne afhænger af hvilket koncept I ønsker. Men vi finder altid en løsning der passer jer bedst.

        Ved arrangement der ikke hører til vores faste lokaliteter kræves tillæg for kørsel.

        Vi har også faste lokaliteter i både København og Århus. Her er transport gratis.
    </p>

    <h1>København</h1>
    <h2>Garderhøjfortet</h2>
    <p>Et flot, autentisk, nyrestaureret fort i Gentofte. Fortet er opvarmet om vinteren. Desuden har fortet et Kanontårn, som kan være en del af aktiviteterne.
        <br>
        <br>
        - 15 minutters kørsel fra indre København.
        <br>- Let at komme til med motorvejen.
        <br>- Aktiviteter både ude og inde.
        <br>- Der skal betales leje for at anvende Garderhøjfortet.
    </p>

    <h2>Kastrupfortet</h2>
    <p>Kastrupfortet ligger på Amager og er meget velegnet til at afholde Fangerne på fortet. Fortet er udelukkende udendørs, så er man til outdoor er dette stedet.
        <br>
        <br>
        - Kastrupfortet er gratis at leje
        <br>- Er kun udendørs
        <br>- Mulighed for at samarbejde med fortets restaurant
    </p>

    <h1>Århus</h1>
    <h2>Århus Privatskole</h2>
    <p>Vi har en fastbase på Århus privatskole.<br><br>
        - Aktiviteter både ude og inde
        <br>- Bade og omklædningsfaciliteter
        <br>- Velegnet til både firmaer og polterabender
    </p>

